#ініціалізація змінної для суми.
sum_of_numbers = 0

#використання циклу for для перебору чисел від 500 до 1000.
for num in range(500, 1001):
    if num % 5 == 0:  #перевірка, чи число кратне 5.
        sum_of_numbers += num

# Вивід результату.
print("Сума натуральних чисел, кратних 5, в діапазоні від 500 до 1000: ", sum_of_numbers)